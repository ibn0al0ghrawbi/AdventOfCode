package dayFour;

public class passport {

	private int byr;
	private int iyr;
	private int eyr;
	private String hgt;
	private String ecl;
	private String hcl; 
	private int pid;
	private int cid;
	
	public passport() {
		
	}
	
	
	private void setByr (int byr) {
		this.byr = byr;
	}
	
	private void setIyr(int iyr) {
		this.iyr = iyr;
	}
	
	private void setEyr(int eyr) {
		this.eyr = eyr;
	}
	
	private void setHgt(String hgt) {
		this.hgt = hgt;
	}
	
	private void setEcl(String ecl) {
		this.ecl = ecl;
	}
	
	private void setHcl(String hcl) {
		this.hcl = hcl;
	}
	
	private void setPid(int pid) {
		this.pid = pid;
	}
	
	private void setCid(int cid) {
		this.cid = cid;
	}
	
}
